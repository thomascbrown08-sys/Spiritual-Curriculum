    <h1>Signposts of Growth</h1>
    <div class="page-subtitle">Seeing God's Faithfulness Over Time</div>
    <div class="sequence-note">Having established that the story is compelling, the document credible, the resurrection real, and the problem of suffering honestly faced — the group is ready for the bridge question: what does following this actually produce in a life? This module transitions from the evidential toward the applied.</div>
  </div>

  <h2>Fruit as Expression, Not Measure</h2>
  <div class="scripture">
    <span class="scripture-ref">Galatians 5:22–23</span>
    <p>"But the fruit of the Spirit is love, joy, peace, patience, kindness, goodness, faithfulness, gentleness, self-control; against such things there is no law."</p>
  </div>
  <p>The Spirit's fruit is exactly that — fruit. You do not manufacture it. You abide in the vine, and fruit is the natural direction of a living connection. This distinction is one of the most practically important in the curriculum: growth is the natural <em>expression</em> of genuine faith, not the <em>measure</em> by which we verify whether our faith is real.</p>
  <p>If growth is the measure of genuine faith, then inconsistency — which Paul describes as his own normal experience in Romans 7 — becomes evidence against one's salvation. But if growth is the natural expression of genuine faith, then inconsistency is a call to return to the vine, not a verdict on whether we were ever truly connected. The root precedes and produces the fruit. Assurance rests on the root — on Christ's faithfulness — not on the consistency of our output.</p>

  <h2>What Growth Actually Looks Like</h2>
  <p>The signposts of genuine growth are rarely dramatic. They tend to be quiet changes over time: a reduction in reactivity, an increase in patience under pressure, a greater capacity to love difficult people, a growing honesty with oneself, an ability to sit with suffering without being destroyed by it. These are observable — by the person experiencing them and by those around them.</p>
  <p>2 Peter 1:5–8 describes a progression — faith to virtue to knowledge to self-control — not as a ladder to climb but as a description of a life that is moving. The direction matters more than the speed. A person who is measurably more patient than they were five years ago, whose anger has lost some of its edge, who finds themselves capable of genuine compassion that did not come naturally before — that person is seeing the Spirit's fruit emerging in the slow time of a real life.</p>

  <div class="box box-pilgrim toggle-pilgrim">
    <div class="box-title">🕊️ When the Growth Is Not Visible</div>
    <p>What about the season when no growth is visible — when the person has been trying faithfully and the fruit seems absent, the vine apparently dry? Module 6 addresses this in the context of suffering. Here it is worth noting the agricultural reality: fruit has seasons. A vine in winter looks dead. The absence of visible fruit in a season is not evidence that the root has gone. The question is not "do I see fruit right now?" but "am I abiding?" — and sometimes the most faithful abiding is the kind that persists through apparent fruitlessness. Eugene Peterson's phrase — "a long obedience in the same direction" — was never about straight-line progress.</p>
    <p class="cross-link-note">See also: <a href="module8.html#experiment">Module 8 — When the Experiment Doesn't Yield Results</a></p>
  </div>

  <div class="box box-facilitator toggle-facilitator">
    <div class="box-title">🧭 The Spirit as Agent of Growth</div>
    <p>Transformation is not primarily the product of our effort — it is the work of the Spirit in cooperation with our willingness. 2 Corinthians 3:18 describes the process: "we all, with unveiled face, beholding the glory of the Lord, are being transformed into the same image from one degree of glory to another. For this comes from the Lord who is the Spirit." The signposts of growth are signposts of the Spirit's work. Encourage the group to notice them with gratitude rather than claim them as personal achievement.</p>
  </div>

  <div class="discussion">
    <div class="discussion-label">Discussion Questions</div>
    <ul>
      <li>Looking back over a period of years, can you identify ways you have changed that you believe are connected to faith? What does that change look like?</li>
      <li>Do you find it more natural to look at fruit to verify the root, or to look to the root and trust the fruit will follow? Which does Scripture encourage?</li>
      <li>Is there someone in your life whose growth over time has been evidence to you of something real happening in them?</li>
    </ul>
